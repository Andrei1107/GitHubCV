package com.example.myapplication;

public class Client
{

    String idClient;
    String numeClient;
    String varstaClient;

    public Client()
    {


    }

    public Client(String idClient, String numeClient, String varstaClient) {
        this.idClient = idClient;
        this.numeClient = numeClient;
        this.varstaClient = varstaClient;
    }

    public String getIdClient() {
        return idClient;
    }

    public String getNumeClient() {
        return numeClient;
    }

    public String getVarstaClient() {
        return varstaClient;
    }





}
